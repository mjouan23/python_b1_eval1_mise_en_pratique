# On concatène "To" et "Ta" => "Tota"
# Puis on le répète 2 fois =>  "ToTaToTa"

b = "To" * 4 
print(b) #ToToToTo
# On répète 4 fois To => ToToToTo

c = "3" + 3 
print(c) #Erreur
# On ne peut pas aditionner un entier et un caractères et 3 est un caractère car entre guillemets

d = 7 // 2 
print(d) # 3
# Dans 7 il y a 3 fois 2, sachant que "//" on ne prend que l'entier

e = 7 % 2
print(e) # 1
# Quand on divise 7 par 2, il reste 1

